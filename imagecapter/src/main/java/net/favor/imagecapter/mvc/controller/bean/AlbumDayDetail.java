package net.favor.imagecapter.mvc.controller.bean;

public class AlbumDayDetail {

	private String day;
	private String dayToken;
	private long thumbKey;
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getDayToken() {
		return dayToken;
	}
	public void setDayToken(String dayToken) {
		this.dayToken = dayToken;
	}
	public long getThumbKey() {
		return thumbKey;
	}
	public void setThumbKey(long thumbKey) {
		this.thumbKey = thumbKey;
	}
	

}
